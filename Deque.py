class Deque:

    def __init__(self):

        self.deque = []

    def addFront(self, item):

        return self.deque.insert(0, item)

    def addTail(self, item):

        return self.deque.append(item)

    def removeFront(self):

        if len(self.deque) != 0:
            return self.deque.pop(0)
        else:
            return None

    def removeTail(self):

        if len(self.deque) != 0:
            return self.deque.pop(-1)
        else:
            return None

    def size(self):

        if len(self.deque) != 0:
            return len(self.deque)
        else:
            return 0

    def palindrom(self, string):

        self.deque += string
        while len(self.deque) > 1:
            if self.removeFront() == self.removeTail():
                continue
            else:
                break
        if len(self.deque) > 1:
            self.deque = []
            print('no palindrom')
        else:
            self.deque = []
            print('palindrom')

